package bobo.obj;

public class HEAD {
	public String D;
	public String E;
	public String F;
	
	public HEAD() {
		super();
	}

	public HEAD(String d, String e, String f) {
		super();
		D = d;
		E = e;
		F = f;
	}

	public String getD() {
		return D;
	}

	public void setD(String d) {
		D = d;
	}

	public String getE() {
		return E;
	}

	public void setE(String e) {
		E = e;
	}

	public String getF() {
		return F;
	}

	public void setF(String f) {
		F = f;
	}
	
	

}